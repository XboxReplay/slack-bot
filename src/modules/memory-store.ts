type MemoryStory = {
    [type: string]: any;
};

const store: MemoryStory = {};

export const getFromStore = <T>(key: string): T | null => {
    const k = store[key];
    return k !== void 0 ? k : null;
};

export const setToStore = (key: string, payload: any) => {
    store[key] = payload;
    return store;
};
